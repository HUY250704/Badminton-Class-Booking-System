const defaultLanguage = 'vi'

export function getLanguage() {
  if (typeof window === 'undefined') return defaultLanguage
  return window.localStorage.getItem('language') === 'en' ? 'en' : 'vi'
}

export function setLanguage(value) {
  if (typeof window === 'undefined') return
  window.localStorage.setItem('language', value)
  window.dispatchEvent(new Event('languagechange'))
}

export function useTranslation() {
  const [language, setLanguageState] = useState(getLanguage())

  useEffect(() => {
    function handleLanguageChange() {
      setLanguageState(getLanguage())
    }

    window.addEventListener('storage', handleLanguageChange)
    window.addEventListener('languagechange', handleLanguageChange)

    return () => {
      window.removeEventListener('storage', handleLanguageChange)
      window.removeEventListener('languagechange', handleLanguageChange)
    }
  }, [])

  return {
    language,
    t: (key, fallback = '') => translations[language]?.[key] || translations[defaultLanguage][key] || fallback,
    setLanguage: (value) => setLanguage(value),
    toggleLanguage: () => setLanguage(language === 'vi' ? 'en' : 'vi')
  }
}

export default translations



